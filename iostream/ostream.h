#include "ios.h"

namespace kick {
	///////////////////////////////////////////////////////////////////////////////
	// basic_ostream : basic_ios
	///////////////////////////////////////////////////////////////////////////////
	template<typename CharT>
	class basic_ostream : public virtual basic_ios<CharT> {
		
	};
	
	///////////////////////////////////////////////////////////////////////////////
	// ostream
	///////////////////////////////////////////////////////////////////////////////
	typedef basic_ostream<char> ostream;
	
	///////////////////////////////////////////////////////////////////////////////
	// wostream
	///////////////////////////////////////////////////////////////////////////////
	typedef basic_ostream<wchar_t> wostream; 
	
} // namespace kick

#endif // _kick_iostream_ostream_h
